package com.example.demo.model;

public class SignIn {
    String mail="";
    String user_Pass ="";

    public void setMail(String mail) {
        this.mail = mail;
    }

    public void setUser_Pass(String user_Pass) {
        this.user_Pass = user_Pass;
    }

    public String getUser_Pass() {
        return user_Pass;
    }

    public String getMail() {
        return mail;
    }
}
